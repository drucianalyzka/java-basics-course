public class Zadanie691 {
	public static void main(String[] args) {
		
		int [] firstTab = {2, 4, 6, 8 };
		int [] secondTab = {1, 3, 5, 7, 9};
		
		for (int i = 0; i < firstTab.length; i++) {
			System.out.println(firstTab[i]);
			i++;
		}
		
		for (int j = 0; j < secondTab.length; j++) {
			System.out.println(secondTab[j]);
			j++;
		}
	
	}
}